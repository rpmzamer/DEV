//empty array
let arr=[];
console.log(arr);

//array with elements

arr=[1,2,3,4,"Hello I am String",false,'c',2.5]
console.log(arr);

//traversing array
console.log(arr[4]);

//updation
arr[3]="changed"
console.log(arr);

//Array Methods

//1) PUSH
arr.push(8);
console.log(arr);

//2) POP
arr.pop();
console.log(arr);

//3) SHIFT
arr.shift();
console.log(arr);

//4) UNSHIFT
arr.unshift('First');
console.log(arr);

//length
let l=arr.length;
console.log(l);
